<template>
  <v-container>
    <v-row>
      <v-col :cols="4">
        <v-text-field
          :label="$t('properties.name')"
          clearable
          v-model="key"
          :disabled="disabled"
        ></v-text-field>
      </v-col>
      <v-col :cols="3">
        <operators-select :disabled="disabled" @selected="setOperator" :operator="operator"></operators-select>
      </v-col>
      <v-col :cols="5">
        <v-combobox v-if="operator === 'in'"
          :disabled="disabled"
          multiple
          clearable
          :placeholder="$t('general.typeToAdd')"
          @change="setValue"
          v-model="value"
        ></v-combobox>
        <v-text-field v-else
          :placeholder="$t('general.typeToAdd')"
          clearable
          v-model="value"
          :disabled="disabled"
        ></v-text-field>
        <v-checkbox
          v-model="includeRawDataHistory"
          :label="$t('query.includeHistoricalRaw')"
          :disabled="disabled"
        />
      </v-col>
    </v-row>
  </v-container>
</template>

<script lang="ts">
import {Component, Prop, Vue, Watch} from "vue-property-decorator";
import OperatorsSelect from "@/components/queryBuilder/operatorsSelect.vue";
@Component({components:{OperatorsSelect}})
export default class RawDataFilter extends Vue {
  @Prop({required: false})
  queryPart?: QueryPart

  @Prop({required: false, default: false})
  disabled?: boolean

  key = ""
  operator = ""
  value = ""
  includeRawDataHistory = true

  beforeMount(){
    if(this.queryPart) {
      this.key = this.queryPart.key
      this.operator = this.queryPart.operator
      this.value = this.queryPart.value
    }
  }

  setOperator(operator: string) {
    this.operator = operator
  }

  setValue(value: any) {
    this.value = value
  }

  get part(): QueryPart {
    return {
      componentName: 'RawDataFilter',
      key: this.key,
      operator: this.operator,
      value: this.value,
      options: {historical: this.includeRawDataHistory}
    }
  }

  @Watch('part')
  onQueryPartChange() {
    if(!this.disabled) {
      this.$emit('update', this.part)
    }
  }
}

// QueryPart matches the type expected by the query builder - this allows us to use Object.assign and copy over on
// changes
type QueryPart = {
  componentName: 'RawDataFilter';
  key: string;
  operator: string;
  value: any;
  nested?: QueryPart[];
  options?: {[key: string]: any}
}
</script>